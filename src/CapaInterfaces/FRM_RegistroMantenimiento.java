    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        System.out.println(tabla_especificaciones_activos.getSelectedRow() + "Este es el numero seleccionado");
        if(tabla_especificaciones_activos.getSelectedRow()!=-1){
            if ("CPU".equals(tabla_especificaciones_activos.getValueAt(tabla_especificaciones_activos.getSelectedRow(), 1)) || "Computadora de escritorio".equals(tabla_especificaciones_activos.getValueAt(tabla_especificaciones_activos.getSelectedRow(), 1)) 
                    || "Port치til".equals(tabla_especificaciones_activos.getValueAt(tabla_especificaciones_activos.getSelectedRow(), 1)) || "Servidores".equals(tabla_especificaciones_activos.getValueAt(tabla_especificaciones_activos.getSelectedRow(), 1))) {
                    registroMantenimiento.activosCambiar();
            }else{
                mensajes.msg_advertencia(this, "Seleccione un activo que cuente con estos componentes");
            }
        }else{
            mensajes.msg_advertencia(this, "Seleccione un activo para poder hacer el cambio");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

